    public void SetValues()
    {
        double vperphy, vpermat, vpereng, vperelec;
        
        sname.setText(StudentName);
        rollno.setText(Rollno);
        obtphy.setText(Physics);
        obteng.setText(English);
        obtelec.setText(Electronics);
        obtmaths.setText(Maths);
        
        //Calculating individual percentages for each subject
        
        vperphy= (Double.parseDouble(Physics) / Double.parseDouble(tphy.getText()))*100;
        perphy.setText(Double.toString(vperphy));
        
                vpermat= (Double.parseDouble(Maths) / Double.parseDouble(tmat.getText()))*100;
        permaths.setText(Double.toString(vpermat));
        
                vpereng= (Double.parseDouble(English) / Double.parseDouble(teng.getText()))*100;
        pereng.setText(Double.toString(vpereng));
        
                vperelec= (Double.parseDouble(Electronics) / Double.parseDouble(telec.getText()))*100;
        perelec.setText(Double.toString(vperelec));
        
        // Set Graded //
        gphy.setText(GetGrade( vperphy));
        geng.setText(GetGrade( vpereng));
        gelec.setText(GetGrade( vperelec));     
        gmat.setText(GetGrade( vpermat));
        
       //Set Total Marks
       double TotalMarks= Double.parseDouble(tphy.getText())+Double.parseDouble(tmat.getText())+Double.parseDouble(teng.getText())+Double.parseDouble(telec.getText());
       double TotalObtMarks= Double.parseDouble(Physics) +Double.parseDouble(Maths) +Double.parseDouble(English) +Double.parseDouble(Electronics) ;
       double totalper = TotalObtMarks/TotalMarks*100;
       totalmarks.setText(Double.toString(TotalMarks));
       totalobt.setText(Double.toString(TotalObtMarks));
       this.totalper.setText(Double.toString(totalper));
       finalgrade.setText(GetGrade( vperphy));
    }
    
    public String GetGrade(double per)
    {
         if(per >= 90)
        {
            return "A+";
        }
         else if (per >=80 && per <90)
         {
             return "A";
         }
          else if (per >=70 && per <80)
         {
             return "B+";
         }
          else if (per >=60 && per <70)
         {
             return "B";
         }
          else if (per >=50 && per <60)
         {
             return "C+";
         }
          else if (per < 50)
         {
             return "Fail";
         }
         
    return "No Grade";
    }
